"""Reproduce every WiFi-TTA-Bench result from prepared data.

This script is the single entry point for the NeurIPS 2026 Evaluations &
Datasets track submission. Each component script is called explicitly; there
are no hidden pre-baked artifacts. Every path in ``ARTIFACT_MANIFEST`` is
regenerated by the command listed in ``SOURCE_SCRIPTS`` unless it is a
structural aggregation (Table 10).

Preconditions:
    * ``data/prepared/{widar_bvp,ntufi_har,signfi_top10}`` populated by the
      per-dataset preparation scripts (see ``SUBMISSION_README.md``).
    * ``pip install -e ".[dev]"`` run from the repo root.

Usage:
    python scripts/reproduce_all_results.py            # skip steps whose
                                                       # output already exists
    python scripts/reproduce_all_results.py --force    # regenerate everything
    python scripts/reproduce_all_results.py --only coverage,figures
                                                       # run a subset

Outputs are written under ``outputs/`` and figures under
``manuscript/paper2/figures/``. A fresh manifest is written to
``outputs/SUBMISSION_MANIFEST.md`` after every successful run.
"""


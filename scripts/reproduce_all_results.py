def main() -> None:
    parser = argparse.ArgumentParser(
        description="Reproduce all WiFi-TTA-Bench paper results."
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Regenerate all components even if outputs already exist.",
    )
    parser.add_argument(
        "--only",
        type=str,
        default=None,
        help="Comma-separated component ids to run (default: all).",
    )
    parser.add_argument(
        "--manifest-only",
        action="store_true",
        help="Skip all components; only write SUBMISSION_MANIFEST.md.",
    )
    args = parser.parse_args()
    force: bool = args.force

    root = Path(__file__).resolve().parent.parent
    logger.info("=" * 72)
    logger.info("WiFi-TTA-Bench: reproduce_all_results (root=%s)", root)
    if force:
        logger.info("  --force: regenerating every component")
    logger.info("=" * 72)

    if args.manifest_only:
        write_submission_manifest(root)
        return

    components = _default_order()
    if args.only:
        wanted = {c.strip() for c in args.only.split(",") if c.strip()}
        unknown = wanted - set(components)
        if unknown:
            logger.error("Unknown component ids: %s", sorted(unknown))
            sys.exit(2)
        components = [c for c in components if c in wanted]

    for component_id in components:
        _run_component(component_id, root, force)

    write_submission_manifest(root)
    logger.info("=" * 72)
    logger.info("DONE. Manifest: outputs/SUBMISSION_MANIFEST.md")
    logger.info("=" * 72)


if __name__ == "__main__":
    main()

# Inline block used by component ``ntufi_signfi_core``: a 7-method primary-MLP
# TTA sweep for NTU-Fi + SignFi. Kept inline because no dedicated script
# exists upstream for this specific cell; output schemas match the Widar
# method_summary.json contract.
_NTUFI_SIGNFI_INLINE = """
import json
import torch
from pathlib import Path
from pinn4csi.training.paper2_tta import (
    TTAExperimentConfig, build_prepared_tta_loaders, run_tta_suite,
)
from pinn4csi.utils.metrics import bootstrap_ci, cohens_d

CORE = ('no_adapt','entropy_tta','tent','physics_tta','selective_physics_tta','shot','t3a')
for dataset, out_dir in [('ntufi_har','outputs/ntufi_tta'),('signfi_top10','outputs/signfi_tta')]:
    results = {}
    config = TTAExperimentConfig(
        methods=CORE, source_epochs=10, adaptation_steps=5, batch_size=32,
        hidden_dim=64, num_layers=3, latent_dim=32,
        selective_confidence_threshold=0.2, selective_alignment_threshold=2.0,
    )
    for train_e, test_e in [((0,1),(2,)), ((0,2),(1,)), ((1,2),(0,))]:
        for seed in range(5):
            s1, s2, t1, t2, ishape, nc, meta = build_prepared_tta_loaders(
                dataset, Path('data/prepared'), seed, 32, 0.2, 0.5,
                train_env_ids=train_e, test_env_ids=test_e,
            )
            rows = run_tta_suite(s1, s2, t1, t2, ishape, nc, meta, seed, config)
            for r in rows:
                results.setdefault(r.method, []).append(r.gain)
    noadapt = torch.tensor(results.get('no_adapt', [0.0]))
    summary = {}
    for m in CORE:
        g = torch.tensor(results.get(m, [0.0]))
        pt, lo, hi = bootstrap_ci(g)
        d = float(cohens_d(g, noadapt)) if m != 'no_adapt' else 0.0
        nr = float((g<0).float().mean().item())
        summary[m] = {'mean_gain': pt, 'ci_lower': lo, 'ci_upper': hi,
                      'cohens_d_vs_noadapt': d, 'negative_adaptation_rate': nr,
                      'n_observations': len(g)}
    Path(out_dir).mkdir(parents=True, exist_ok=True)
    with open(f'{out_dir}/method_summary.json','w') as f:
        json.dump(summary, f, indent=2)
    print(f'{dataset} saved to {out_dir}')
"""


# ---------------------------------------------------------------------------
# Execution helpers
# ---------------------------------------------------------------------------


def _all_outputs_exist(root: Path, paths: list[str]) -> bool:
    return all((root / p).is_file() for p in paths)


def _run_argv(argv: list[str], description: str, root: Path) -> None:
    cmd = [sys.executable] + argv
    logger.info(">>> %s", description)
    logger.info("    $ %s", " ".join(cmd))
    result = subprocess.run(cmd, cwd=root, capture_output=True, text=True)
    if result.returncode != 0:
        logger.error(
            "FAILED: %s\n  Exit %d\n  Stderr:\n%s",
            description,
            result.returncode,
            result.stderr[-3000:] if result.stderr else "(no stderr)",
        )
        sys.exit(result.returncode)
    logger.info("    OK: %s", description)


def _run_inline(code: str, description: str, root: Path) -> None:
    logger.info(">>> %s", description)
    result = subprocess.run(
        [sys.executable, "-c", code], cwd=root, capture_output=True, text=True
    )
    if result.returncode != 0:
        logger.error(
            "FAILED: %s\n  Exit %d\n  Stderr:\n%s",
            description,
            result.returncode,
            result.stderr[-3000:] if result.stderr else "(no stderr)",
        )
        sys.exit(result.returncode)
    logger.info("    OK: %s", description)


def _run_component(component_id: str, root: Path, force: bool) -> None:
    spec = SOURCE_SCRIPTS[component_id]
    outputs: list[str] = list(spec.get("outputs", []))  # type: ignore[arg-type]
    description = str(spec["description"])

    if not force and outputs and _all_outputs_exist(root, outputs):
        logger.info("    SKIP (outputs exist): %s", description)
        return

    argv = spec.get("argv")
    if component_id == "ntufi_signfi_core":
        _run_inline(_NTUFI_SIGNFI_INLINE, description, root)
    elif component_id == "aggregation":
        logger.info(">>> %s (no-op)", description)
    elif argv is None:
        logger.warning(
            "Component %s has no argv and no inline handler; skipping.", component_id
        )
    else:
        _run_argv(list(argv), description, root)  # type: ignore[arg-type]

    # Validate that every declared output signal exists after running.
    missing = [p for p in outputs if not (root / p).is_file()]
    if missing:
        logger.error("COMPONENT OUTPUT MISSING after %s: %s", component_id, missing)
        sys.exit(4)


def write_submission_manifest(root: Path) -> None:
    out_dir = root / "outputs"
    out_dir.mkdir(parents=True, exist_ok=True)
    manifest_path = out_dir / "SUBMISSION_MANIFEST.md"
    lines = [
        "# Submission Artifact Manifest",
        "",
        "Generated by `scripts/reproduce_all_results.py`. Every artifact",
        "below is regenerated by the listed component; the `Status` column",
        "reflects whether the output file exists on disk at manifest time.",
        "",
        "| Paper Element | Artifact Path | Component | Status |",
        "| --- | --- | --- | --- |",
    ]
    for label, path, component in ARTIFACT_MANIFEST:
        exists = (root / path).exists() if not path.startswith("aggregated ") else True
        status = "present" if exists else "missing"
        lines.append(f"| {label} | `{path}` | `{component}` | {status} |")
    lines.append("")
    lines.append(
        "No artifacts are marked PRE-BAKED: each is reproduced by its "
        "declared component script."
    )
    manifest_path.write_text("\n".join(lines))
    logger.info("Manifest written to: %s", manifest_path)


